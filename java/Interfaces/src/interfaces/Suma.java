package interfaces;

/**
 *
 * @author Carlos
 */
public class Suma implements ICalculo {
    int a;
    int b;

    public Suma(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    @Override
    public void calcular() {
        int c=a+b;
        System.out.println("la suma es:"+c);
    }
    
    
}
